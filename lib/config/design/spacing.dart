/// A class that defines the spacing values used in the application.
abstract class AppSpacing {
  /// The extra small spacing value.
  static const double xssmall = 5;

  /// The small spacing value.
  static const double small = 10;

  /// The extra medium spacing value.
  static const double xmedium = 16;

  /// The medium spacing value.
  static const double medium = 20;

  /// The extra large spacing value.
  static const double xlarge = 25;

  /// The large spacing value.
  static const double large = 30;

  /// The extra large spacing value.
  static const double extraLarge = 40;
}
